// comperision operators
// ==, ===, !=, !==, <=, >=
var a = 10
var b = 15
console.log(a == b); // false
b = 10
console.log(a == b); // true
console.log(a != b); // false
a = 15
console.log(a != b); // true
b = 25
console.log(a >= b); // false
console.log(a <= b); // true
a = 25
console.log(a >= b); // true
console.log(a <= b); // true

a = "50"
b = 50
console.log(a == b); // it will play true
console.log(a === b); // it will play false
console.log(a !== b); // it will play true